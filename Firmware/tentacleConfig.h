//Defines pins to be used for stepper motor control & bluetooth software serial connection

#define STEP_EN               8    // Enables/disables all stepper drivers
#define STEP_RL               2    // Steps right or left
#define STEP_FB               4    // Steps forward or backward
#define RL_MOTOR_A_DIR        5    //Sets right-left motor A direction
#define RL_MOTOR_B_DIR        6    //Sets right-left motor B direction (always opposite RL motor A)
#define FB_MOTOR_A_DIR        7    //Sets forward-backward motor A direction
#define FB_MOTOR_B_DIR        13   //Sets forward-backward motor B direction (always opposite FB motor A)
